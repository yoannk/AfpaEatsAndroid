package com.example.afpaeats.Entities;

public class TypeCuisine {
    private int idTypeCuisine;
    String nom;
    String photo;

    public int getIdTypeCuisine() {
        return idTypeCuisine;
    }

    public void setIdTypeCuisine(int idTypeCuisine) {
        this.idTypeCuisine = idTypeCuisine;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }
}
